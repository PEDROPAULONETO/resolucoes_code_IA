# Vamos solicitar como entrada dois números e depois vamos realizar uma operação simples entre eles.

# Solicita a entrada do primeiro número e converte para float
num1 = float(input("Digite o primeiro número: "))

# Solicita a entrada do segundo número e converte para float
num2 = float(input("Digite o segundo número: "))

# Realiza a soma dos dois números
resultado = num1 + num2

# Exibe o resultado da operação
print(f"O resultado da soma de {num1} + {num2} é: {resultado}")
